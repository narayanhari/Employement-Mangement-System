    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        for(User ob:Employee_Management.users){
            if(ob.getEmpno().equals(empno)){
                ob.setAccess(1);
                ob.setDept(jTextField4.getText());
                ob.setDesignation(jTextField3.getText());
                ob.setEmpno(jTextField1.getText());
                ob.setName(jTextField2.getText());
                ob.setSalary(Integer.valueOf(jTextField5.getText()));
                ob.setPhone(jTextField6.getText());
                ob.setPassword(jTextField7.getText());
            }
        }
        
        JOptionPane.showMessageDialog(null,"User Updated");
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

